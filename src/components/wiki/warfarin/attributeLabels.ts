function humanizeKey(key: string): string {
  return key
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/Hp/g, 'HP')
    .replace(/Sp/g, 'SP')
    .replace(/Atb/g, 'ATB')
    .replace(/Cd/g, 'CD')
    .replace(/Dmg/g, 'DMG');
}

export function getWarfarinAttributeLabel(attrType: string | number, locale = 'en-US'): string {
  const numeric = Number(attrType);
  const key = Number.isFinite(numeric)
    ? attributeTypeNames[numeric]
    : typeof attrType === 'string'
      ? attrType
      : '';
  if (!key) return String(attrType);
  if (locale.startsWith('zh')) return zhCN[key] || humanizeKey(key);
  if (locale.startsWith('ja')) return jaJP[key] || humanizeKey(key);
  return humanizeKey(key);
}
